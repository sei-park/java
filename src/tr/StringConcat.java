package tr;

public class StringConcat {

	public static void main(String[] args) {
		
		String stringVal1 = "jdk";
		String stringVal2 = "1.17";
		String stringVal3 = "10"; 
		
		int a = 1; 
	
		String stringVal4 = stringVal1 + stringVal2;
		System.out.println(stringVal4);
		
		int stringVal6 = Integer.parseInt(stringVal3) + a;
		System.out.println(stringVal6);
				
		
	}

}
