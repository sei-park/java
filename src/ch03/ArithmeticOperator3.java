package ch03;

public class ArithmeticOperator3 {

	public static void main(String[] args) {
		
		// 산술 연산자(=, -, *, /, %)
		
			int v1 = 5;
			int v2 = 2;
				
			System.out.println(v1 + v2);
			System.out.println(v1 - v2);
			System.out.println(v1 * v2);
			System.out.println(v1 / v2);
			System.out.println(v1 % v2);

	}

}
