package ch02;

public class FloatDoubleEx {

	public static void main(String[] args) {
		
		float var1 = 3.14F;
		
		double var2 = 3.14;
		
		System.out.println(var1);
		System.out.println(var2);
		
		// 정밀도 테스트
		double var4 = 0.12345678;
		float var5 = 0.12345678F;
		
		System.out.println(var4);
		System.out.println(var5);
	}

}
    